package operators;

public class Arithmetic_operators {

	public static void main(String[] args) {
		/**
		 * Arithmetic operators are used to perform mathematical operations
		 */
	int num1 = 24, num2 = 12;
	int Add, Sub, Div, Mul, Qut;
	
	Add = num1 + num2; // ADDITION
	System.out.println(Add);
	
	Sub = num1 - num2; //Subtraction
	System.out.println(Sub);
	
	Mul = num1 * num2; //Multiplication
	System.out.println(Mul);
	
	Div = num1 / num2; // Division:  when / is used , it returns in quotient 
	System.out.println(Div);
	
	Qut = num1 % num2; //Division:  when % is used , it returns in remainder
	System.out.println(Qut);
		
	}

}
