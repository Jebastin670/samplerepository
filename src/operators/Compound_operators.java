package operators;

public class Compound_operators {

	public static void main(String[] args) {
		/**
		 * --------Assignment operators--------
		 */
		int x = 10; // = is simple assignment operator
		
		x += 2; // x=x+2 ----> O.P is 12
		
		x -= 2; // x=x-2 ----> O.P is 8
		
		x *= 2; // x=x*2 ----> O.P is 20
		
		x /= 2; // x=x/2 ----> O.P is 5
		
		x %= 2; // x=x%2 ----> O.P is 0 
		
		
		
		
	}

}
